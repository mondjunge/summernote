      this.show = function() {
        context.invoke('editor.saveRange');
        this.showEmoticonsDialog().then(function(emoji) {
          context.invoke('editor.restoreRange');

          var $node = document.createTextNode(emoji);
          if ($node) {
            context.invoke('editor.insertNode', $node);
          }
        }).fail(function() {
          context.invoke('editor.restoreRange');
        });
      };

      /**
       * Show the emoticons dialog and return a Promise resolving with the chosen emoji character.
       *
       * @return {Promise}
       */
      this.showEmoticonsDialog = function() {
        return $.Deferred(function(deferred) {
          var $dialog = self.$dialog;

          ui.onDialogShown($dialog, function() {
            $dialog.on('click.emoticons', '.note-emoticon-node', function(event) {
              event.preventDefault();
              var value = $(event.currentTarget).find('button').attr('data-value');
              if (value) {
                deferred.resolve(value);
                ui.hideDialog($dialog);
              }
            });
          });

          ui.onDialogHidden($dialog, function() {
            $dialog.off('click.emoticons');

            if (deferred.state() === 'pending') {
              deferred.reject();
            }
          });

          ui.showDialog($dialog);
        });
      };
